// ============================================
// 生成简体到繁体的映射表
// 通过交换键值对，从繁体表生成简体表
// ============================================
const SIMPLIFIED_MAP = Object.fromEntries(
    Object.entries(TRADITIONAL_MAP).map(([k, v]) => [v, k])
);

// ============================================
// 繁体转简体
// 功能：将繁体中文字符转换为简体
//
// 参数：
//   - text: 要转换的文本
//
// 返回值：转换后的简体文本
// ============================================
function toSimplified(text) {
    // 如果是数组，递归处理每个元素后合并
    if (Array.isArray(text)) {
        return text.map(toSimplified);
    }
    // 如果不是字符串，转换为字符串后处理
    if (typeof text !== 'string') {
        text = String(text);
    }
    // 逐字符转换
    return text.split('').map(char => SIMPLIFIED_MAP[char] || char).join('');
}

// ============================================
// 简体转繁体
// 功能：将简体中文字符转换为繁体
//
// 参数：
//   - text: 要转换的文本
//
// 返回值：转换后的繁体文本
// ============================================
function toTraditional(text) {
    if (Array.isArray(text)) {
        return text.map(toTraditional);
    }
    if (typeof text !== 'string') {
        text = String(text);
    }
    return text.split('').map(char => TRADITIONAL_MAP[char] || char).join('');
}

// ============================================
// 计算歌词行分段数
// 功能：计算一行歌词被分割成多少段（用于行号计算）
//
// 分割规则（与前端渲染逻辑一致）：
// 1. 连续重复词中间空格 → 不分割（如 等 等 等）
// 2. 英文单词间空格 → 不分割（如 You make me cry）
// 3. 空格前汉字数 < 3 → 不分割
// 4. 其他空格 → 分割
// 5. 书名号《》和括号（）内空格 → 不分割
//
// 参数：
//   - chars: 字符数组，歌词行的字符列表
//
// 返回值：分割后的段数
// ============================================
function countSegments(chars) {
    // 特殊情况处理
    if (!chars || !chars.length) return 0;

    let segments = 1;  // 至少有一段
    let inBrackets = 0;  // 括号嵌套层级
    let charCountSinceLastSpace = 0;  // 自上次空格以来的汉字计数
    let prevWord = '';  // 空格前的累积词

    // 遍历每个字符
    for (let i = 0; i < chars.length; i++) {
        const char = chars[i];

        // 检测括号开始
        if (char === '《' || char === '(' || char === '（') {
            inBrackets++;
        }
        // 检测括号结束
        else if (char === '》' || char === ')' || char === '）') {
            inBrackets = Math.max(0, inBrackets - 1);
        }
        // 检测空格（仅在括号外处理）
        else if ((char === ' ' || char === '\u3000') && inBrackets === 0) {
            // 收集空格后的下一个词
            let nextWord = '';
            for (let j = i + 1; j < chars.length; j++) {
                if (chars[j] === ' ' || chars[j] === '\u3000') break;
                nextWord += chars[j];
            }

            // 规则1：连续重复词中间空格 → 不分割
            if (nextWord && prevWord === nextWord) {
                // 不分割，继续累积
            }
            // 规则2：英文单词间空格 → 不分割
            else if (/^[a-zA-Z]/.test(prevWord) && /^[a-zA-Z]/.test(nextWord)) {
                // 不分割
            }
            // 规则3：空格前汉字数 < 3 → 不分割
            else if (charCountSinceLastSpace < 3) {
                // 不分割
            }
            // 规则4：其他空格 → 分割
            else {
                segments++;
            }

            // 重置计数，开始新的词
            charCountSinceLastSpace = 0;
            prevWord = nextWord;  // 空格后的词成为新的 prevWord
        } else {
            // 非空格字符，累积汉字计数
            if (/[\u4e00-\u9fff\u3400-\u4dbf]/.test(char)) {
                charCountSinceLastSpace++;
            }
            // 累积 prevWord（非空格字符）
            // 如果前一个字符是空格，重置 prevWord 开始新词
            if (i > 0 && (chars[i - 1] === ' ' || chars[i - 1] === '\u3000')) {
                prevWord = char;
            } else {
                prevWord += char;
            }
        }
    }

    return segments;
}

